#!/usr/bin/env python3
"""
Demonstrates the print() function with multiple arguments.

This script shows how print() handles multiple string arguments, automatically
inserting a space between them and outputting all on a single line.
"""

def main():
    """
    Main function to print a nursery rhyme using multiple arguments in print().

    The print() function combines the arguments with spaces and outputs them
    on one line, demonstrating itd default behavior.
    """
    print("The itsy bitsy spider","climbed up","the waterspout.")


if __name__ == "__main__":
    main()
