#!/usr/bin/env python3
"""
Demonstrates basic usage of the print() function with multiple invocations.

This script prints two lines of a nursery rhyme, showcasing that each print()
call starts on a new line and that instructions execute in order.
"""

def main():
    """
    Main function to print two lines of a nursery rhyme.

    Each print() invocation outputs a string on a new line, executed sequentially.
    """
    print("The itsy bitsy spider climbed up the waterspout.")
    print("Down came the rain and washed the spider out.")


if __name__ == "__main__":
    main()
