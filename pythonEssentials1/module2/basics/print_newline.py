#!/usr/bin/env python3
"""
Demonstrates the use of newline (\n) and escape characters in the print() function.

This script prints a nursery rhyme with embedded newline character and shows
how to print a backslash using escaping.
"""


def main():
    """
    Main function to print a nursery rhyme with newline and an escaped backslash.

    Uses \n to insert newlines and \\ to print a single backslash.
    """
    print("The itsy bitsy spider\nclimbed up the waterspout.")
    print("Down came the rain\nand washed the spider out.")
    print("A single backslash: \\")  # Demonstrates escaping the backslash


if __name__ == "__main__":
    main()
